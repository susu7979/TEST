package TEST1;

public class string {

	public static void main(String[] args) {
		String string="Susu is a pretty girl.";
		System.out.println(string.charAt(3));
		System.out.println(string.contains("s"));
		System.out.println(string.replace('s', 'h'));
		System.out.println(string.toCharArray());
		System.out.println(string.toLowerCase());
		System.out.println(string.toUpperCase());
		System.out.println(string.length());
		System.out.println(string.startsWith("a"));
		System.out.println(string.split(" ")[0]);
	}

}
